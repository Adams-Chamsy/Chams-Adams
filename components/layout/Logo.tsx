import Link from 'next/link';
import { cn } from '@/lib/utils';

export type LogoVariant = 'mark' | 'wordmark' | 'full' | 'stacked';

export type LogoProps = {
  /** Niveau sémantique. Header/Footer → link vers /, sinon `as="div"`. */
  as?: 'link' | 'div';
  /**
   * Variante visuelle :
   *  - `mark`     : monogramme circulaire seul (favicon, header compact)
   *  - `wordmark` : signature script "Chams Adams" seule (header desktop)
   *  - `full`     : monogramme + wordmark côte-à-côte (email header)
   *  - `stacked`  : monogramme empilé au-dessus du wordmark (footer signature)
   */
  variant?: LogoVariant;
  /**
   * Hauteur globale en px. Pour `wordmark` : hauteur du texte.
   * Pour `mark` : hauteur du monogramme.
   * Pour `full` / `stacked` : hauteur du monogramme (wordmark proportionné).
   */
  size?: number;
  className?: string;
};

/**
 * Logo officiel de la Maison.
 *
 * Deux SVG inline (pas de fetch, pas de FOUC) :
 *  - <Monogram /> : cercle C&A en dégradé or (4 Ko)
 *  - <Wordmark /> : "Chams Adams" en script calligraphié (6 Ko)
 *    Fill = `currentColor` → s'adapte au contexte (ivoire sur fond noir,
 *    noir sur fond clair dans l'email).
 */
export function Logo({
  as = 'link',
  variant = 'wordmark',
  size,
  className,
}: LogoProps) {
  // Tailles par défaut selon la variante
  const defaultSize =
    variant === 'mark' ? 44 : variant === 'wordmark' ? 36 : variant === 'stacked' ? 72 : 44;
  const s = size ?? defaultSize;

  let content: React.ReactNode;

  switch (variant) {
    case 'mark':
      content = <Monogram heightPx={s} />;
      break;

    case 'wordmark':
      content = <Wordmark heightPx={s} />;
      break;

    case 'full':
      content = (
        <span className="inline-flex items-center gap-3">
          <Monogram heightPx={s} />
          <Wordmark heightPx={Math.round(s * 0.65)} />
        </span>
      );
      break;

    case 'stacked':
      content = (
        <span className="inline-flex flex-col items-center gap-4">
          <Monogram heightPx={s} />
          <Wordmark heightPx={Math.round(s * 0.55)} />
        </span>
      );
      break;
  }

  if (as === 'div') {
    return (
      <span
        role="img"
        aria-label="Chams Adams"
        className={cn('inline-flex items-center text-ivoire', className)}
      >
        {content}
      </span>
    );
  }

  return (
    <Link
      href="/"
      aria-label="Chams Adams — Accueil"
      data-cursor="hover"
      className={cn(
        'inline-flex items-center text-ivoire transition-opacity duration-300 hover:opacity-90',
        className
      )}
    >
      {content}
    </Link>
  );
}

